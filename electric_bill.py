#โปรแกรมคำนวณค่าไฟ

#รับจำนวนหน่วยไฟฟ้าจากผู้ใช้
unit = float(input("กรอกจำนวนหน่วยค่าไฟฟ้า: "))
#คำนวณค่าไฟฟ้า โดยคิดหน่วยละ 4.50 บาท
bill = unit * 4.5
#ตัดทศนิยมให้เหลือ2ตำแหน่งโดยไม่ปัดเศษ
bill = int(bill * 100) / 100
#แสดงผลค่าไฟฟ้าเป็นทศนิยมสองตำแหน่งโดยใช้.2f
print("ค่าไฟฟ้า = %.2f บาท" % bill )